import Foundation

class Day2Part2 {
    private static let winPoint = 6
    private static let drawPoint = 3
    private static let losePoint = 0
    
    private static let winPairs = [GamePair(opponent: Shape.C, you: Shape.X),
                           GamePair(opponent: Shape.B, you: Shape.Z),
                           GamePair(opponent: Shape.A, you: Shape.Y)]
    private static let drawPairs = [GamePair(opponent: Shape.A, you: Shape.X),
                            GamePair(opponent: Shape.B, you: Shape.Y),
                            GamePair(opponent: Shape.C, you: Shape.Z)]
    private static let losePairs = [GamePair(opponent: Shape.A, you: Shape.Z),
                            GamePair(opponent: Shape.B, you: Shape.X),
                            GamePair(opponent: Shape.C, you: Shape.Y)]
    
    class func calculatePoints() {
        let games = input.components(separatedBy: CharacterSet.newlines)

        var totalPoints = 0
        for game in games {
            let trimmedGame = game.trimmingCharacters(in: .whitespacesAndNewlines)
            let players = trimmedGame.components(separatedBy: " ")
            if players.count != 2 {
                continue
            }
            guard let opponentShape = Shape.initialize(stringValue: players[0]),
                  let resultShape = Shape.initialize(stringValue: players[1]) else {
                continue
            }

            switch resultShape {
            case .Y: // draw
                if let pair = drawPairs.filter({ $0.opponent == opponentShape }).first {
                    totalPoints += drawPoint
                    totalPoints += pair.you.value
                }
            case .Z: // win
                if let pair = winPairs.filter({ $0.opponent == opponentShape }).first {
                    totalPoints += winPoint
                    totalPoints += pair.you.value
                }
            default: // default lose
                if let pair = losePairs.filter({ $0.opponent == opponentShape }).first {
                    totalPoints += losePoint
                    totalPoints += pair.you.value
                }
                break
            }
        }
        print(totalPoints)
    }
    
